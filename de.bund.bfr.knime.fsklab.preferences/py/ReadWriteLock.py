import threading

# An implementation of a read-write-lock.
class ReadWriteLock:

    def __init__(self):
        self._lock = threading.Condition()
        self._readers = 0
        self._writer = False

    def acquire_read(self):
        self._lock.acquire()
        while self._writer:
            self._lock.wait()
        self._readers += 1
        self._lock.release()

    def release_read(self):
        self._lock.acquire()
        self._readers -= 1
        self._lock.notify_all()
        self._lock.release()

    def acquire_write(self):
        self._lock.acquire()
        while self._writer or self._readers > 0:
            self._lock.wait()
        self._writer = True
        self._lock.release()

    def release_write(self):
        self._lock.acquire()
        self._writer = False
        self._lock.notify_all()
        self._lock.release()
